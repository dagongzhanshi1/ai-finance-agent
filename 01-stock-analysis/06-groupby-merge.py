import pandas as pd

# =============================================
# 第一部分：groupby（分组统计）
# =============================================
data = {
    "股票": ["茅台", "茅台", "五粮液", "五粮液", "宁德", "宁德"],
    "年份": [2024, 2025, 2024, 2025, 2024, 2025],
    "营收_亿": [1500, 1700, 800, 850, 4000, 5000],
    "净利润_亿": [750, 850, 250, 270, 500, 600]
}
df = pd.DataFrame(data)
print("=== 原始数据 ===")
print(df)

# groupby：按股票分组，取净利润的平均值
print("\n=== 按股票分组，算平均净利润 ===")
print(df.groupby("股票")["净利润_亿"].mean())
result=df.groupby("股票")["净利润_亿"].mean()
print(type(result))

# agg：同时算多个统计量
print("\n=== 按股票分组，算营收总和 + 净利润均值 ===")
print(df.groupby("股票").agg({
    "营收_亿": "sum",
    "净利润_亿": "mean"
}))
print(type(df))

# =============================================
# 第二部分：merge（合并两张表）
# =============================================
price_df = pd.DataFrame({
    "股票代码": ["600519", "000858", "300750"],
    "收盘价": [1800, 150, 220]
})
pe_df = pd.DataFrame({
    "股票代码": ["600519", "000858", "300750"],
    "市盈率": [30, 25, 50]
})

print("\n=== 股价表 ===")
print(price_df)
print("\n=== 市盈率表 ===")
print(pe_df)

# 按股票代码合并
merged = pd.merge(price_df, pe_df, on="股票代码")
print("\n=== 合并结果 ===")
print(merged)

