import pandas as pd

data = {
    "股票": ["茅台", "茅台", "五粮液", "五粮液", "宁德", "宁德"],
    "年份": [2024, 2025, 2024, 2025, 2024, 2025],
    "营收_亿": [1500, 1700, 800, 850, 4000, 5000],
    "净利润_亿": [750, 850, 250, 270, 500, 600]
}
df = pd.DataFrame(data)

# ===== 练习 1 =====
print("=" * 50)
print("练习 1：按年份分组，算每年的总营收和平均净利润")
print("=" * 50)
# 你的代码写在这里👇
result1=df.groupby("年份").agg({"营收_亿":"sum","净利润_亿":"mean"})
print(result1)
# ===== 练习 2 =====
print("=" * 50)
print("练习 2：按股票分组，算净利润率（净利润/营收）")
print("提示：先在原表加一列 '净利润率'，再按股票分组取平均")
print("=" * 50)
# 你的代码写在这里👇
df["净利润率"]=df["净利润_亿"]/df["营收_亿"]
result2=df.groupby("股票")["净利润率"].mean()
print(result2)
# ===== 练习 3 =====
price_df = pd.DataFrame({
    "股票代码": ["600519", "000858", "300750", "002594"],
    "收盘价": [1800, 150, 220, 350]
})
pe_df = pd.DataFrame({
    "股票代码": ["600519", "000858", "300750"],
    "市盈率": [30, 25, 50]
})

print("=" * 50)
print("练习 3：两张表 merge，保留左表全部股票")
print("左表有 4 只，右表只有 3 只")
print("提示：用 how='left'")
print("=" * 50)
# 你的代码写在这里👇
result3=pd.merge(price_df,pe_df,on="股票代码",how='left')
print(result3)